/******************************************************************************
 *                                                                            *
 *                          　モールスコード変換関数                            *
 *                                                                            *
 ******************************************************************************/
const char* sMorseGetCode(char c) {
    if (c < 0 || c >= ASCII_TABLE_SIZE) {
        return NULL;  // 範囲外の文字はNULLを返す
    }
    return MORSE_TABLE[(int)c];
}